        { id = "p3",    x = 320, y = 420, w = 150, h = 24, type = "static" },
        -- p4：左上方，通向终点（安全路线从 p3 跳上来，技巧路线从 p2 补枪飞到）
        { id = "p4",    x = 150, y = 540, w = 140, h = 24, type = "static" },
    },
