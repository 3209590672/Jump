        -- p1：右侧低台，安全路线第一跳（容易到达）
        { id = "p1",    x = 330, y = 180, w = 160, h = 24, type = "static" },
        -- p2：左上方，安全路线和技巧路线的分叉点
        { id = "p2",    x = 80,  y = 300, w = 160, h = 24, type = "static" },
