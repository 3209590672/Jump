-- 入职校准 0-3：空中补枪救险
-- 教学目标：让玩家发现"空中可以再开一枪修正方向"
--
-- 核心设计原则（与产品对齐）：
--   - 普通玩家：不补枪也能通关，走安全路线（多跳几次）
--   - 熟练玩家：补枪可以走捷径，少跳平台，拿 S/A
--   - 失败玩家：补枪失误掉到低层平台而非直接死亡
--
-- 两条路线：
--   安全路线：start → p1 → p2 → p3 → p4 → finish（全程地面开火）
--   技巧路线：start → p2 → (空中补枪) → p4 → finish（跳过中间平台）
--
-- 布局示意：
--
--                   [finish]
--              [p4]
--        [p3]           ← 安全路线中转
--   [p2]
--              [p1]     ← 安全路线第一跳
--   [start]
--
-- ============================================================================
